function completarSequencias(): void {
    console.log("a) Próximo número é 9 (incremento de 2)");
    console.log("b) Próximo número é 128 (dobro do anterior)");
    console.log("c) Próximo número é 49 (quadrados perfeitos)");
    console.log("d) Próximo número é 100 (quadrados perfeitos)");
    console.log("e) Próximo número é 13 (sequência de Fibonacci)");
    console.log("f) Próximo número é 20 (sequência, sem padrão definido mas incremento variável)");
}

completarSequencias();

// Respostas
// a) 1, 3, 5, 7, 9 (sequência crescente de 2 em 2)
// b) 2, 4, 8, 16, 32, 64, 128 (sequência de múltiplos de 2)
// c) 0, 1, 4, 9, 16, 25, 36, 49 (quadrados perfeitos)
// d) 4, 16, 36, 64, 100 (quadrados perfeitos)
// e) 1, 1, 2, 3, 5, 8, 13 (sequência de Fibonacci)
// f) 2, 10, 12, 16, 17, 18, 19, 20 (incremento com padrão variado)